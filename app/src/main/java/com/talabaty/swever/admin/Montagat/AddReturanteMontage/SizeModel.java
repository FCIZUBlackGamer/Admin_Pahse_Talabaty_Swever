package com.talabaty.swever.admin.Montagat.AddReturanteMontage;

public class SizeModel {
    String id;
    String size;

    public SizeModel(String id, String size) {
        this.id = id;
        this.size = size;
    }

    public String getId() {
        return id;
    }

    public String getSize() {
        return size;
    }
}
